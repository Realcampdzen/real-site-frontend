function shouldSkipRateLimit(req) {
  const ip = req.ip || req.connection?.remoteAddress || '';
  const origin = req.get('origin') || '';
  
  // –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø–æ IP (localhost)
  const isLocalhostIP = ip === '127.0.0.1' || 
                        ip === '::1' || 
                        ip === '::ffff:127.0.0.1' || 
                        ip.includes('localhost') ||
                        ip.startsWith('127.') ||
                        ip.startsWith('::ffff:127.');
  
  // –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø–æ origin
  const isLocalhostOrigin = origin && (
    origin.startsWith('http://localhost:') || 
    origin.startsWith('http://127.0.0.1:')
  );
  const isGitHubPages = origin && origin.includes('realcampdzen.github.io');
  
  return isLocalhostIP || isLocalhostOrigin || isGitHubPages;
}

// Cooldown middleware - –º–∏–Ω–∏–º–∞–ª—å–Ω–∞—è –∑–∞–¥–µ—Ä–∂–∫–∞ 2 —Å–µ–∫—É–Ω–¥—ã –º–µ–∂–¥—É —Å–æ–æ–±—â–µ–Ω–∏—è–º–∏
const lastRequestTime = new Map();

function cooldownMiddleware(req, res, next) {
  if (shouldSkipRateLimit(req)) return next();
  
  const ip = req.ip;
  const lastTime = lastRequestTime.get(ip) || 0;
  const now = Date.now();
  
  if (now - lastTime < 2000) {
    logger.warn(`Cooldown violation for IP: ${ip}`);
    return res.status(429).json({ 
      error: '–°–ª–∏—à–∫–æ–º –±—ã—Å—Ç—Ä–æ, –ø–æ–¥–æ–∂–¥–∏—Ç–µ 2 —Å–µ–∫—É–Ω–¥—ã',
      retryAfter: 2
    });
  }
  
  lastRequestTime.set(ip, now);
  next();
}

